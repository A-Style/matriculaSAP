    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed

        log.setUser(txtUsu.getText());
        log.setPass(txtPass.getText());
        Boolean rpt = log.ingresar();

        if (!rpt) {
            JOptionPane.showMessageDialog(null, "tu Usuario o Contrase√±a no son correctas");
            txtPass.setText("");
            txtUsu.setText("");
        } else {

            setIdUsuario(log.idEmpleado());
            setNomUsuario(log.nomCompletoEmpleado());

            setDniUsuario(txtUsu.getText());

            JOptionPane.showMessageDialog(null, "Bienvenido");

            lblUsuario.setText("Usuario : " + getNomUsuario());
            contenedor.setEnabledAt(0, false);
            contenedor.setEnabledAt(1, true);
            contenedor.setSelectedIndex(1);

            txtUsu.setText("");
            txtPass.setText("");
            txtBuscar.requestFocus();

        }


    }//GEN-LAST:event_btnLoginActionPerformed

